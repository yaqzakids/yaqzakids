import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..')
const envPath = path.join(root, '.env')
const outPath = path.join(root, 'src', 'generated', 'supabase.public.ts')

function readEnvFile(filePath) {
  if (!fs.existsSync(filePath)) return {}
  const env = {}
  for (const line of fs.readFileSync(filePath, 'utf8').split('\n')) {
    const trimmed = line.trim()
    if (!trimmed || trimmed.startsWith('#')) continue
    const separator = trimmed.indexOf('=')
    if (separator === -1) continue
    env[trimmed.slice(0, separator).trim()] = trimmed.slice(separator + 1).trim()
  }
  return env
}

const env = readEnvFile(envPath)

const lines = [
  '// Auto-generated by scripts/generate-supabase-public.mjs — do not edit manually',
  `export const PUBLIC_SUPABASE_URL = ${JSON.stringify(env.VITE_SUPABASE_URL || 'https://cgvzeydhhkwosphixznd.supabase.co')}`,
  `export const PUBLIC_SUPABASE_ANON_KEY = ${JSON.stringify(env.VITE_SUPABASE_ANON_KEY || '')}`,
  `export const PUBLIC_SITE_URL = ${JSON.stringify(env.VITE_SITE_URL || 'https://www.yaqzakids.com')}`,
  '',
]

if (!env.VITE_SUPABASE_ANON_KEY) {
  console.warn('[generate-supabase-public] VITE_SUPABASE_ANON_KEY missing in .env')
}

fs.mkdirSync(path.dirname(outPath), { recursive: true })
fs.writeFileSync(outPath, lines.join('\n'))
console.log('[generate-supabase-public] wrote src/generated/supabase.public.ts')
